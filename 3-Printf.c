#include <stdio.h>

int main(){

    printf("Hello"); // Print Function
    printf("\n"); // for a blank line
    printf("Hello\nWorld\n"); // \n use for printing  the info on the new line

    // Format Specifier

    printf("My number : %d\n", 500); // %d - Integer
    printf("My name : %s\n", "Pradumna"); // %s - string
    printf("My name : %f\n", 500.12);  // %f - floating point number
    printf("My grade : %c\n", 'A');    // %c is for char
    printf("My name %s and number %d\n", "Pradumna", 400);





}